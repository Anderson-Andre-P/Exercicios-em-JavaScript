// Faça um programa que receba três valores reais e imprima a soma dos quadrados desses três números.

const num1 = parseFloat(prompt('Digite um número'));
const num2 = parseFloat(prompt('Digite um número'));
const num3 = parseFloat(prompt('Digite um número'));

alert(`${num1} ^ 2 + ${num2} ^ 2 + ${num3} ^ 2 = ${(num1 ** 2) + (num2 ** 2) + (num3 ** 2)}`)
